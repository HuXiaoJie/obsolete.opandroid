package com.openpeer.javaapi;

public class OPCaptureCapability {
    private int mWidth;
    private int mHeight;
    private int mMaxFPS;
    
	public int getWidth() {
		return mWidth;
	}
	public void setWidth(int mWidth) {
		this.mWidth = mWidth;
	}
	public int getHeight() {
		return mHeight;
	}
	public void setHeight(int mHeight) {
		this.mHeight = mHeight;
	}
	public int getMaxFPS() {
		return mMaxFPS;
	}
	public void setMaxFPS(int mMaxFPS) {
		this.mMaxFPS = mMaxFPS;
	}
}
